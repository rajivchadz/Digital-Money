package com.project.walletsystem.simulation.beans;

public class UserBankBean {

	private long account_no;
	private String account_name;
	private String bank_name;
	private String ifsc_code;
	private long card_no;
	private int date_on_card;
	private int year_on_card;
	private String name_on_card;
	public long getAccount_no() {
		return account_no;
	}
	public void setAccount_no(long account_no) {
		this.account_no = account_no;
	}
	public String getAccount_name() {
		return account_name;
	}
	public void setAccount_name(String account_name) {
		this.account_name = account_name;
	}
	public String getBank_name() {
		return bank_name;
	}
	public void setBank_name(String bank_name) {
		this.bank_name = bank_name;
	}
	public String getIfsc_code() {
		return ifsc_code;
	}
	public void setIfsc_code(String ifsc_code) {
		this.ifsc_code = ifsc_code;
	}
	public long getCard_no() {
		return card_no;
	}
	public void setCard_no(long card_no) {
		this.card_no = card_no;
	}
	public int getDate_on_card() {
		return date_on_card;
	}
	public void setDate_on_card(int date_on_card) {
		this.date_on_card = date_on_card;
	}
	public int getYear_on_card() {
		return year_on_card;
	}
	public void setYear_on_card(int year_on_card) {
		this.year_on_card = year_on_card;
	}
	public String getName_on_card() {
		return name_on_card;
	}
	public void setName_on_card(String name_on_card) {
		this.name_on_card = name_on_card;
	}
	
}
